

/** \brief Funcion para mostrar menu
 *
 * \param a float Primer valor cargado
 * \param b float Segundo valor cargado
 * \return int Devuelve la opcion elegida
 *
 */
int menuUsuario(float a, float b);

/** \brief Funcion para cargar numero
 *
 * \param msj[] char Muestra texto de ingreso
 * \return float Devuelve el valor cargado
 *
 */
float pedirnum(char msj[]);

/** \brief Funcion para mostrar operaciones
 *
 * \param a float Primer operando
 * \param b float Segundo operando
 * \return void
 *
 */
void operaciones(float a , float b);

/** \brief Funcion para sumar
 *
 * \param a float Primer operando
 * \param b float Segundo operando
 * \return float Devuelve el resultado de sumar a + b
 *
 */
float sumar( float a, float b);

/** \brief Funcion para restar
 *
 * \param a float Primer operando
 * \param b float Segundo operando
 * \return float Devuelve el resultado de restar a - b
 *
 */
float restar(float a, float b);
/** \brief Funcion para multiplicar
 *
 * \param a float Primer operando
 * \param b float Segundo operando
 * \return float Devuelve el resultado de la multiplicacion a * b
 *
 */
float multiplicar(float a, float b);
/** \brief Funcion para dividir
 *
 * \param a float Primer operando
 * \param b float Segundo operando
 * \return float Devuelve el resultado de la divicion a / b
 *
 */
float dividir(float a, float b);

/** \brief Funcion para factorial
 *
 * \param n float valor a factorial
 * \return int Devuelve el resultado de factorial n
 *
 */
int factorial(float n);

